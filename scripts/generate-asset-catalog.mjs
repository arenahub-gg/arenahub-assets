// Generates catalog/assets-catalog.md from assets/*/pack.json + file trees.
// Deterministic output (sorted), forward-slash relative paths for portability.
// Usage: npm run catalog
import { readdir, readFile, writeFile, stat } from "node:fs/promises";
import { join, relative, extname } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = fileURLToPath(new URL("..", import.meta.url));
const assetsDir = join(repoRoot, "assets");
const outFile = join(repoRoot, "catalog", "assets-catalog.md");

// File types grouped for readability; anything else lands in "other"
const TYPE_BY_EXT = {
  ".png": "sprites", ".jpg": "sprites", ".gif": "sprites", ".svg": "sprites",
  ".ogg": "audio", ".mp3": "audio", ".wav": "audio",
  ".ttf": "fonts", ".otf": "fonts", ".woff": "fonts", ".woff2": "fonts",
  ".glb": "3d-models", ".gltf": "3d-models", ".fbx": "3d-models", ".obj": "3d-models", ".mtl": "3d-models",
};
const IGNORED_FILES = new Set(["pack.json", "desktop.ini", "thumbs.db"]);

async function walkFiles(dir) {
  const out = [];
  for (const entry of await readdir(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) out.push(...(await walkFiles(full)));
    else if (!IGNORED_FILES.has(entry.name.toLowerCase())) out.push(full);
  }
  return out;
}

function toPosix(p) {
  return p.split("\\").join("/");
}

async function main() {
  const packDirs = [];
  for (const entry of await readdir(assetsDir, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    const packJsonPath = join(assetsDir, entry.name, "pack.json");
    try {
      await stat(packJsonPath);
      packDirs.push(entry.name);
    } catch {
      console.warn(`skip ${entry.name}: no pack.json`);
    }
  }
  packDirs.sort();

  const lines = [
    "# Assets Catalog",
    "",
    "Generated by `npm run catalog` — do not edit by hand.",
    "Search this file by keyword (grep) to find asset paths. Paths are repo-relative.",
    "",
  ];

  for (const packDir of packDirs) {
    const packRoot = join(assetsDir, packDir);
    const meta = JSON.parse(await readFile(join(packRoot, "pack.json"), "utf8"));
    lines.push(`## ${meta.name} (\`${meta.id}\`)`);
    lines.push("");
    lines.push(`- License: ${meta.license} | Source: ${meta.source}`);
    lines.push(`- Style: \`${meta.style}\` | Tags: ${(meta.tags ?? []).join(", ")}`);
    if (meta.notes) lines.push(`- Notes: ${meta.notes}`);
    lines.push("");

    const files = (await walkFiles(packRoot))
      .map((f) => toPosix(relative(repoRoot, f)))
      .sort();
    const byType = new Map();
    for (const f of files) {
      if (f.toLowerCase().endsWith("license.txt")) continue;
      const type = TYPE_BY_EXT[extname(f).toLowerCase()] ?? "other";
      if (!byType.has(type)) byType.set(type, []);
      byType.get(type).push(f);
    }
    for (const [type, list] of [...byType.entries()].sort()) {
      lines.push(`### ${type} (${list.length})`);
      lines.push("");
      for (const f of list) lines.push(`- ${f}`);
      lines.push("");
    }
  }

  await writeFile(outFile, lines.join("\n") + "\n", "utf8");
  console.log(`wrote ${toPosix(relative(repoRoot, outFile))} (${packDirs.length} packs)`);
}

await main();
